class Lesson8_1 {
  public static void main(String[] args) {
    String[] arr;
    arr = new String[3];

    // データ型[] 配列変数名;
    // 配列変数名 = new データ型[要素数];


    arr[0]="sato";
    arr[1]="suzuki";
    arr[2]="takahashi";

    System.out.println(arr[0]);
    System.out.println(arr[1]);
    System.out.println(arr[2]);

  }
}